#include <stdlib.h>

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	while (s[i] != (char)c)
	{
		if (!i)
			return (NULL);
		i--;
	}
	return ((char *)s + i);
}
